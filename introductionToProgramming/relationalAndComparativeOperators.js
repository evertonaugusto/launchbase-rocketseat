// >  Maior que
// < Menor que
// >= Maior igual
// <= Menor igual
// == Iguar a
// === Igual e do mesmo tipo
// != Diferente de
// !== Diferente, inclusive do tipo

console.log(5 > 4);
console.log(5 < 4);
console.log(5 >= 4);
console.log(5 <= 4);
console.log(5 == 4);
console.log(5 == "4");
console.log(5 === "4");
console.log(5 != "4");
console.log(5 !== "4");

const age = 18;

if (age >= 18) {
  console.log("Deixa entrar!");
} else {
  console.log("Bloqueia a entrada!");
}

if (age === 17) {
  console.log("Volte assim que tiver 18 anos.");
}
